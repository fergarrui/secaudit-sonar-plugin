package fg.sonar.plugins.secaudit.rules.checks;

import com.google.common.collect.ImmutableList;
import java.util.List;
import org.sonar.check.Rule;
import org.sonar.plugins.java.api.IssuableSubscriptionVisitor;
import org.sonar.plugins.java.api.tree.ClassTree;
import org.sonar.plugins.java.api.tree.Tree;
import org.sonar.plugins.java.api.tree.TypeTree;

@Rule(key = "MessageDigestCustomCheck")
public class MessageDigestCustomCheck extends IssuableSubscriptionVisitor {

  private static final String MESSAGE_DIGEST_FULLY_NAME = "java.security.MessageDigest";

  @Override
  public List<Tree.Kind> nodesToVisit() {
    return ImmutableList.of(Tree.Kind.CLASS);
  }

  @Override
  public void visitNode(Tree tree) {
    ClassTree classTree = (ClassTree)tree;
    TypeTree superClassTree = classTree.superClass();
    if (superClassTree == null) {
      return;
    }
    if (superClassTree.symbolType().is(MESSAGE_DIGEST_FULLY_NAME)) {
      reportIssue(superClassTree, "Using a custom MessageDigest can be dangerous. It's better to use recommended hashing algorithms.");
    }
  }
}
